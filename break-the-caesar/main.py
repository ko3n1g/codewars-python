import collections
import string


def break_caesar(message: str) -> int:
    group_by_len = collections.defaultdict(list)

    for word in sorted(WORDS):
        group_by_len[len(word)].append([ord(c) for c in word])

    message_words = (
        message.lower().translate(str.maketrans("", "", string.punctuation)).split(" ")
    )
    message_words = [[ord(c) for c in word] for word in message_words]

    final_candidates = []
    for message_word in message_words:
        # Modulo since caesar is cyclic
        candidates = [
            [(message_word[i] - c) % 26 for i, c in enumerate(candidate)]
            for candidate in group_by_len[len(message_word)]
        ]

        # A candidate can be identified if all chars have been shifted
        # by the same amount. Hence the mean shift value among all
        # chars is the same as the shift value of any given char.
        candidates = [
            candidate[0]
            for candidate in candidates
            if sum(candidate) / len(candidate) == candidate[0]
        ]

        final_candidates.append(candidates)

    # Most likely are the shift values present in all words.
    # Second attempt: Maybe just pick the shift value which appears most often
    group_by_value = collections.defaultdict(int)
    for candidates in final_candidates:
        for candidate in set(candidates):
            group_by_value[candidate] += 1

    return max(group_by_value.items(), key=lambda v: v[1])[0]


